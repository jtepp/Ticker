int combineLetters(int* a, int* b) {
  int aLength = sizeof(a) / sizeof(int);
  int bLength = sizeof(b) / sizeof(int);
  for (int i = aLength; i < aLength + bLength; i++) {
    a[i] = b[i];
  }

  return bLength;  // add this to sentence length
}